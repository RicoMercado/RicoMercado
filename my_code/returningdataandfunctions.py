def get_sum(num1, num2):
    return num1 + num2

print('\nSumming Two Numbers')
num1 = int(input('\nEnter the first number: '))
num2 = int(input('\nEnter the second number: '))
print(f'\nThe sum of {num1} and {num2} is {get_sum(num1, num2)}')
